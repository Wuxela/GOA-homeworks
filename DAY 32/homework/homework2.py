# ამოცანა 1: დაწერეთ ფუნქცია, რომელიც მიიღებს ორ რიცხვს და დააბრუნებს მათ ჯამს.
def add_numbers(a, b):
    return a + b

# ამოცანა 2: დაწერეთ ფუნქცია, რომელიც მიიღებს სტრინგს და დააბრუნებს მისი სიმბოლოების რაოდენობას.
def count_characters(s):
    return len(s)

# ამოცანა 3: დაწერეთ ფუნქცია, რომელიც მიიღებს სიას და დააბრუნებს სიაში ყველაზე დიდ ელემენტს.
def find_maximum(lst):
    return max(lst)

# ამოცანა 4: დაწერეთ ფუნქცია, რომელიც მიიღებს რიცხვს და დააბრუნებს მისი ფაქტორიალს.
def factorial(n):
    if n == 0 or n == 1:
        return 1
    else:
        return n * factorial(n - 1)

# ამოცანა 5: დაწერეთ ფუნქცია, რომელიც მიიღებს სტრინგს და დააბრუნებს მას უკუღმა.
def reverse_string(s):
    return s[::-1]